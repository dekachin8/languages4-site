                  <h1>Revitalizing Kwetlal: Integrating Traditional Lekwungen Agricultural Practices Through Language and Cultural Education</h1>
                <p class="pl-4 my-6 border-left border-500 border-2x font-italic font-weight-normal gray-600 lead">Given the devastating impacts of cultural loss on Indigenous peoples, there is an urgency to restore and regenerate cultural practices and reconnect to Indigenous homelands for the future sustainability and survival of Indigenous communities. This article explores how the traditional agricultural practices of the Lekwungen (lÉ™kÌ“Ê·É™Å‹É™n) People in their ancestral homelands, also known as Victoria (Metulia) and Greater Victoria, Canada, particularly those involving the culturally significant quetzal (camas), are being revived. This process bridges past and present to nourish future generations.</p>  
                  
                <h4>Traditional Kwetlal Cultivation</h4>
                  
                <p>The kwetlal, a beautiful blue lily that blooms in April and May with a starchy bulb that has been a staple food and trade item for the Lekwungen people for generations, stands as a testament to the resilience and agricultural expertise of the community.  Historically, this food was celebrated, harvested, pit-cooked, and traded with people up and down the coast. It is unique and was highly sought after. Lekwungen women have been the backbone of this food system, managing it for centuries and sustaining their communities through their deep connections to and management of traditional homelands.</p>
                
                <h4>Sustainable Practices:</h4>  
                  
                <li><strong>Land Management:</strong> The Lekwungen people managed the Garry Oak Meadows ecosystems where kwetlal thrived, using controlled burns and removal of invasive plant species "pulls," among other techniques, to maintain the health of the land. These techniques directly address climate change and fire management.</li>
                
                <li><strong>Cultural Transmission: </strong> The knowledge of kwetlal cultivation and preparation has been passed down from mothers to daughters, ensuring the continuation of these practices. Traditionally, community governance was done through family groups, not a chief or council, and everyone had a voice. Women oversaw the work on the land, planting, harvesting, and preparing for trade.</li>
                                   
                <br><p>Despite significant impacts from settler colonial encroachment, which has reduced the kwetlal food system to less than 5% of its original yield over 150 years ago, efforts continue to restore and revitalize this essential part of Lekwungen culture.</p>
                  
                <p> Cheryl Bryce, a member of the Songhees Nation of the Lekwungen ancestral land, and her family have been managing these territories for generations. She is leading by creating a "Community Tool Shed" focused on education and reinstating indigenous food systems. There is a vital educational component to this work; she has developed maps of Victoria with traditional places' place names and has spoken to several school groups and residents about the region's history and their obligations to the kwetlal food systems. </p> 
                  
                <p>Cheryl explains that sustainability is more than creating plans; it is about conserving and protecting the land. It's about people, connections, stories, songs, and using and trading.  It's about reinstating food systems, sustaining the practices, and understanding why they're important to us.</p> 
                  
                <p>A significant motivation for kwetlal restoration is health.  With diabetes crippling around 60% of the community, there is a need to find alternatives.  "Kwetlal is a wonderful form of starch because as you cook it, it is much easier on the body to digest than the potato." Cheryl developed this further in an award-winning documentary she produced in 2003, Diabetes: Then and Now.</p> 
                  
                <h4>Cultural Significance and Language Revitalization</h4>  
                  
                <p>Land-based education, often described as "on-the-land" learning, profoundly resonates with the practices and cultural teachings of the Lekwungen people. This educational approach, which emphasizes a profound connection to the land and its resources, perfectly exemplifies the Lekwungen's cultivation and revitalization of kwetlal. These practices are about growing kwetlal and nurturing a connection to the land that supports spiritual, cultural, and community well-being.</p>  

                <p>As land-based education seeks to integrate Indigenous knowledge systems into learning experiences, the Lekwungen community uses these practices to teach younger generations about their history, language, and traditions. The kwetlal fields serve as living classrooms where the land becomes a teacherâ€”illustrating lessons on ecology, botany, and the ethics of care and respect for the environment.</p>
                  
                <p>By restoring traditional kwetlal cultivation practices, which are culturally and historically significant, educators and community leaders can provide a tangible experience of land-based education. This approach aligns with the broader goals of Language 4 projects, which aim to engage learners with Indigenous thought systems and reconnect them with their ancestral lands. The work in these fields embodies the scaffolded immersion pedagogy by breaking down complex ecological and linguistic concepts into manageable, engaging learning activities that progress from essential vocabulary acquisition to more profound, more integrated dialogues about cultural and environmental stewardship.</p>  

                <p>By incorporating examples of traditional practices into the curriculum, educators ensure that the language and teachings passed down through generations are preserved and adapted to meet modern challenges, reinforcing the community's identity and continuity.</p>      
                  
                <h4>Challenges and Opportunities</h4>  
                  
                <p>The challenges facing the restoration of traditional practices are significant, from climate change impacting growing conditions and increasing development and destruction of harvesting sights to the need for educational resources that accurately represent Indigenous ecological knowledge. However, the work of community leaders and collaborations with various organizations present opportunities to overcome these obstacles. Through many efforts, traditional lands preserve biodiversity and serve as living classrooms for youth.</p>
                  
                <h4>Mission of Languages 4</h4>  
                  
                <p>Languages 4 is dedicated to supporting Indigenous language preservation and revitalization efforts. By integrating traditional ecological knowledge into language education, Languages 4 aims to create immersive learning experiences that connect students to their cultural and environmental heritage, promoting sustainability and respect for Indigenous knowledge systems.</p>      

                <h4>Conclusion</h4>  
                  
                <p>As we look to the future, we're working with Tribal communities to develop ways of integrating traditional cultivation practices and ways of knowing into language and cultural education that can enrich learning experiences, preserve biodiversity, and strengthen community resilience. The kwetlal fields of Lekwungen territory, with their deep roots and resilient nature, symbolize the Lekwungen people's enduring connection to their land and culture, offering lessons of the past to nourish future generations.</p>       

                <p>Drawing on these rich examples of Indigenous ecological wisdom, we can create more holistic and impactful language education programs that honor and preserve the deep connections between people, land, and language.</p>                                
                  
