		<div class="container mb-3"><img src="../pages/assets/img/blog_photos/founder_tim_white_shirt1000.webp" class="img-fluid rounded" alt="Langauges 4 Founder, Tim O'Hagan in a white shirt"><p align="center" class="small">Languages 4 founder Tim O'Hagan, in a white button-up shirt, smiling.</p></div><br>

		<p class="text-sans-serif text-1200">July 13, 2023</p>
                  <h1>Languages 4 Founder Tim O'Hagan on Revitalizing Indigenous Languages</h1>
                <p class="pl-4 my-6 border-left border-500 border-2x font-italic font-weight-normal gray-600 lead">I love what I do, and I am confident we can be impactful members of the Indigenous language community. Therefore, I have really enjoyed the opportunities to speak with new people and communities and get the process started. It is hard and requires much patience, but the work is worth it. <br><stong>Tim O'Hagan</stong></p>
                  
                <p>In this edition of What are Languages 4?, we are excited to share a candid Q&amp;A with the founder behind Languages 4, Tim O’Hagan. As the founder of this initiative, Tim carries a personal connection to Indigenous language revitalization and a firm belief in its transformative power. The creation of Languages 4 was born out of a unique journey in language technology and a deep-seated desire to make a lasting impact in the lives of Indigenous communities. In this interview, Tim shares the motivations, triumphs, and challenges behind Languages 4 while also sharing future aspirations. This conversation hopes to express Tim’s unwavering dedication to supporting endangered Indigenous languages and highlights the potential of Languages 4 to inspire positive change.</p>
                  
                <h5>What was the primary driving force that led you to transition from working on English as a Second Language to focusing on endangered Indigenous languages?</h5>
                  
                <p><br>Some might say it was a lucky happenstance, but my career path led me to work with Indigenous language reclamation. In fact, it was an unforeseen lay-off that connected me with my first opportunity to work with the Chickasaw language project at Rosetta Stone. Working with the incredible Chickasaw language team introduced me to a new world of impactful language development, and I was hooked. Interestingly, while very different, English as a Second Language projects and Indigenous language reclamation share similar and incredible benefits: dramatic improvements in mental health, academic success, employability, and self-confidence. Shortly after beginning work with the Chickasaw project (which was a tiny percentage of my work responsibility), I recognized that this was the work I was meant to do. </p>
                
               <h5>How has your connection to the Dakota People influenced your work and the formation of Languages 4?</h5>
                  
                <p><br>While we always knew that we had some Native American ancestry, it wasn't until a conversation with my dying grandmother a few years ago that I began to understand our true lineage. Since her passing, we have learned much about our family tree and actual heritage, to the point that we have met relatives. It certainly has increased my awareness and interest in working on Indigenous language reclamation and influenced the creation of Languages 4. It has also been a great learning experience for me and an opportunity to understand better the true history of Native Americans in the United States (and Canada).</p>
                <p>At the same time, it is also a very personal challenge because I have only encountered the challenges of the devastating history of Native Americans through the experiences of others. While I share a heritage, I have not lived a shared experience, and I struggle with that. I do think that has boosted my motivation for Languages 4, though, because I want to positively influence the Native communities in this one area that I do know and understand, language technology. </p>
                  
                <h5>Can you talk about a moment when you realized the profound impact of connecting Indigenous peoples to their heritage languages?</h5>
                  
                <p><br>There have been a few impactful moments, including working with an incredible advocate of the Ojibwe language who shared his personal and powerful journey or the devastatingly lonely conversation I had in Milwaukee with the very last first-language speaker of a tribe in California. <br>
                 However, let me quickly share a compelling story. I was meeting with the leadership council from a tribe in California, and we were going to all break for lunch. While deciding where to eat, one of the members "jokingly" stated that three individuals were not allowed to drive in the same car. When I asked why, they told me that they three were the last speakers of their language and couldn't risk any accident as a group.</p>
                <p>I was astonished, not just because they seemed so cavalier about it, but because that was the first time I was faced with the truth about the direness of Indigenous languages. Unfortunately, that situation is very prevalent across North America. And while there is certainly no magic bullet, we can do much to improve the situation. </p>
                <H5>You mentioned that working on Indigenous languages helps increase the community's pride, self-confidence, and self-worth. Can you share a particular story or instance that really exemplifies this?</H5> 
                  <p><br>This is something that I believe wholeheartedly from my interactions with many community members and through reading and research. The history and process of colonization in North America have been devastating and comprehensive, led perhaps by the experiences of the boarding schools. First, as an American student, I was embarrassingly unaware of these experiences before my work, and I am disgusted by the lack of our education on the matter. While it is not for me to say about the consequential loss of language and identity, I have met, read, and experienced some incredible people and their stories about how their language journeys have improved and even saved their lives. I have been incredibly inspired by many people I have been lucky enough to work with. </p>
                  <h5>You created Languages 4 after seeing a need for more resources for large-scale Indigenous language projects. How does Languages 4 strive to fill this gap?</h5>
                 <p><br>First, let me reiterate that we have reached an inflection point for most Indigenous languages in North America.  Systematic and intentional government policies, in conjunction with unethical industrial activity, have led to what UNESCO has termed as the Decade of Action for Indigenous language.  The skill gap between the elderly generation and those below is dire and will require an unprecedented investment and intervention.</p><p>Languages 4 offers a multifaceted approach, combining research-based methodologies, innovative technology, and cultural representation.  Our platform is rich in media resources, provides interactive lessons across the proficiency spectrum, and ensures accessibility via 24/7 cloud-based multi-platform applications.  We prioritize cultural respect by ensuring our materials honor Indigenous traditions authentically.  Furthermore, our platform fosters community and collaboration, allowing learners and educators to connect and share insights, enhancing the collective language reclamation effort.  In essence, Languages 4 bridges the resource gap through a blend of technology, cultural sensitivity, and community-driven learning and does so on a large scale.</p>
                  <h5>Pricing and development time were significant hurdles you mentioned while working at Rosetta Stone. How has Languages 4 addressed these challenges in its approach?</h5>
                  <p><br>The biggest difference I wanted to bring to the market from my time at Rosetta Stone was a lower price point and a much faster development process. There simply aren't enough communities able to spend several million dollars and 5-10 years developing language projects. We can and are committed to offering exceptional projects at roughly 25% of the cost and 20% of the development time, with more content hours and equal or better efficacy. This is done through a different methodology and less expensive media.</p>
                  <p>I feel very fortunate to have SMASH Education as our platform partner. Their Founder, Abraham Bravo, is committed and dedicated to the mission, and his team is outstanding. His product development over the past decade-plus and their commitment to efficacy has helped make our mission possible. I am very excited to have them as a partner. </p>
                  <h5>How is the mission of Languages 4 aligned with the UNESCO International Decade of Indigenous Languages 2022 – 2032?</h5>
                  <p><br>Like the UNESCO Decade of Action, Languages 4 was built with the idea that this next decade is critical. Along with our partner SMASH Education, we are committed to working on as many projects as possible, with a rough goal of 20-30 language projects over the next decade. The focus is on efficacy, engagement, affordability, and rapid delivery. We want learners of all ages engaged as soon as possible. </p>
                  <h5>What specific goals do you hope to achieve with Languages 4 over this decade?</h5>
                  <h6>We have two specific targets:</h6>1) Partner with as many communities in North America as possible to develop language projects.<br><br>2) Take the expertise gained here, and expand to support Indigenous language revitalization and preservation in communities worldwide.</p>
                  <h5>How does Languages 4 cater to languages and communities that might not typically be considered for language technology resources?</h5>
                  <p><br>Let me frame this answer in addition to the question of cost because most communities have not been able to afford the high cost of developing quality custom language learning projects at scale, and we are very proud to offer a much lower price point.</p> 
                  <p>Additionally, due to the design and methodology of Languages 4 projects, they are simply less resource intensive. They are easier to develop with the use of animation and augmented reality in place of live video (which may still be a part of the curriculum). The L4 development team also takes on most of the pedagogical design burden, enabling projects for communities without dedicated language programs.</p>
                  <h5>You expressed a desire to expand the reach of Languages 4 to other regions, like New Zealand and Australia. Can you elaborate on your plans for this?</h5>
                  <p><br>I have always been interested in Australia and New Zealand. It would be a bucket list opportunity for me to work with the Aboriginal languages of Australia and the Maori people in New Zealand, and we can be very impactful. However, our priority at the moment is North America.</p>
                  <h5>What has been the most challenging part of establishing and growing Languages 4?</h5>
                  <p><br>Tough question. Spreading the word and developing new relationships is tough. This is only successful if we can be strong and successful partners; obviously, there is a huge trust factor in that. It is a complex journey. </p>
                  <h5>Conversely, what has been the most rewarding part of this journey so far?</h5>
                  <p><br>I love what I do, and I am confident we can be impactful members of the Indigenous languages reclamation community. Therefore, I have really enjoyed the opportunities to speak with new people and communities and get the process started. It is hard and requires much patience, but the work is worth it. </p>
                  <h5>What message do you want leaders in Native American communities to understand about Languages 4 and its mission?</h5>
                  <p>We understand that language reclamation presents many challenges. There are political land mines, questions of linguistic agreements, economic limitations, and more. But these projects are worth it, and we want to be strong and long-term partners in the effort. We want to do so by being trustworthy, straightforward, and above all else, delivering on what we promise, and we are confident that we can do so. </p>
                  <h5>Looking to the future, where do you see Languages 4 in ten years?</h5>
                  <p><br>Aspirationally, I'd love to do this again in ten years and share the stories of many successful language partnerships with language learners across the generations showcasing their success stories. And I'd love to share stories of successful language projects from the First Nations of Canada, the tribes from the U.S. and Mexico, and from across the sea. There is so much need and so many opportunities. We just can't wait! </p>
                
