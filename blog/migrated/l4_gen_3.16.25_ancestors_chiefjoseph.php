                  <h1>HinmatÃ³owyalahtqÌ“it (Chief Joseph) of the Nez Perce: A Leader for His People, An Example for All</h1>
                <p class="pl-2 my-4 border-left border-500 border-2x font-italic font-weight-normal gray-600 lead">Chief Joseph, known in his language as HinmatÃ³owyalahtqÌ“it ("Thunder Traveling to Loftier Heights"), stands as one of the most influencial voices against the systematic erasure of Indigenous sovereignty. A leader of the Wallowa band of the Nez Perce, he was a strategist in war and an uncompromising diplomat in peace. His name is etched in the annals of history for his resistance during the Nez Perce War of 1877 and his tireless advocacy for his people's dignity and rights in the face of U.S. government betrayal.</p>

                <p class="pl-2 my-4 border-left border-500 border-2x font-italic font-weight-normal gray-600 lead"><strong>"The earth, my mother and nurse, is too sacred to be valued or sold for gold, and my bands have suffered wrong" (Chief Joseph, qt. in Warren 278).</strong></p><br>
                
                <H4>The Legacy of His Father: A Land Promised, A Land Betrayed</H4>  
                  
                <p>Joseph was born around 1840 in the Wallowa Valley, a land of winding rivers, rolling hills, and an ecosystem that has sustained the Nez Perce for countless generations. His father, Old Joseph (Tuekakas), had embraced peace and sought to protect their homeland through diplomacy. He once stood in solemn agreement, trusting the promises made by American officials that the Wallowa Valley would be Nez Perce land "always" (Jain 122).</p>
                  
                <p>But promises written in ink could not withstand the tides of greed.</p> 
                
                <p>In 1863, the U.S. government, eager to open the region to settlers, forced a new treaty that shrank the Nez Perce reservation to a fraction of its original size. Many bands, including Joseph's, never agreed to this so-called "Steal Treaty" (West 9). As his father lay dying, he imparted words that would become the foundation of Joseph's leadership:</p>

                <p class="pl-2 my-4 border-left border-500 border-2x font-italic font-weight-normal gray-600 lead"> "My father held my hand, and he died. Dying, said: 'Think always of your country. Your father has never sold your country. Has never touched white man's money that they should say they have bought the land you now stand on. You must never sell the bones of your fathers'" (Warren 276).</p>
                  
                <p>With these words, Joseph inherited the chieftaincy and the responsibility of resistance.</p>
             
                  
                <center><img src="/pages/assets/img/blog_photos/nez_perce_warrioronhorse600h.webp" class="img-fluid rounded" loading="lazy alt="Nez PercÃ© man, wearing loin cloth and moccasins, on horseback (Curtis, 1905)"/><br>
                <small>A Nez PercÃ© man on horseback (Curtis, 1905)</small><br>  </center> 
                  
                <br><H4>The 1877 War: A Betrayal, A Fight for Survival</H4>
                  
                <p>By 1877, the U.S. government issued an ultimatum: Joseph and his people had 30 days to abandon their homeland and move onto the designated reservation (Wells 35). He prepared to comply with deep reluctance, understanding that armed resistance would only bring devastation. However, before the departure could take place, a small group of Nez Perce warriors, enraged by past injustices, killed white settlers. The U.S. Army seized upon the incident as a pretext for war.  What followed was one of the most extraordinary military campaigns in American history.</p>
                    
                <p>Though often falsely mythologized as a "Red Napoleon," Chief Joseph was not a war chief in the traditional sense. His role was that of a protector, a negotiator, and a strategist in ensuring the survival of his people. Under the leadership of Looking Glass, White Bird, and other war chiefs, the Nez Perce embarked on a 1,100-mile retreat through the Rocky Mountains, outmaneuvering and defeating the U.S. Army in multiple engagements (Wells 36).</p>    
                
                <p>The Nez Perce sought sanctuary with the Crow, a neighboring tribe with longstanding trade and diplomatic ties. As they fled the relentless pursuit of the U.S. Army, they hoped to find refuge in Crow territory, located in present-day Montana and Wyoming. However, due to their own challenges and political pressures, the Crow were unable to offer the assistance the Nez Perce had hoped for. With no other options, the Nez Perce turned toward Canada, aiming to reach Sitting Bull and the Lakota. Just 40 miles from the border, they were ultimately surrounded in the Bear Paw Mountains.</p>    
                
                <p>There, after a five-day siege, Chief Joseph delivered the words that would be immortalized:</p>    
                    
                <p class="pl-2 my-4 border-left border-500 border-2x font-italic font-weight-normal gray-600 lead">"The earth, my mother and nurse, is too sacred to be valued or sold for gold, and my bands have suffered wrong" (Chief Joseph, qt. in Warren 278).</p><br>
                    
                <h4>The Aftermath: Exile, Diplomacy, and Betrayal</h4>    
                    
                <p>Despite the surrender, Joseph believed he had negotiated safe passage for his people back to their homeland.  Instead, they were sent to disease-ridden camps in Kansas and then to Oklahoma, where nearly a quarter of their people perished.  In 1878, the U.S. Indian Commission noted that Joseph's people had suffered immensely, stating:</p>
                    
                <p><i>"The bad effects of their location were manifested in the prostration at once of two hundred and sixty of them; and in a few months in the death of a quarter of the entire number" (Report of the Indian Commission, qtd. in Warren 302).</i></p>    
                    
                <p>Joseph, however, did not resign to despair.  His war shifted from the battlefield to the halls of Washington, D.C.  He lobbied Congress, met with President Rutherford B. Hayes, and delivered speeches that shook the nation's conscience.  He articulated the Nez Perce case not just as a grievance but as a profound indictment of U.S. policy:</p>

                <p><i>""When I think of our condition my heart is heavy.  I see men of my race treated as outlaws and driven from country to country... We only ask an even chance to live as other men live.  Let me be a free manâ€”free to travel, free to stop, free to work... free to think and talk and act for myselfâ€”and I will obey every law, or submit to the penalty" (Chief Joseph, qtd. in Warren 305).</i></p> 
                  
                <p>His words stirred sympathy but did not move policymakers to justice.  Though some Nez Perce were eventually allowed to return to the Northwest, Joseph and his followers were denied access to their Wallowa homeland.  Instead, they were relocated to the Colville Reservation in Washington, among tribes with no ancestral ties (West 6).</p>  
                  
                <h4>The Last Fight: A Man Exiled from His Land</h4>  
                  
                <p>Joseph never stopped advocating for his people's return to Wallowa.  He traveled east again in 1903 to plead his case, but the government remained indifferent (West 10).  When he died in 1904, the reservation doctor recorded his cause of death as "a broken heart" (Warren 310).  Yet his spirit never surrendered.</p> 
                  
                <p>His resistance was not only one of survival but also of sovereignty.  He understood the tactics of the U.S. governmentâ€”how they used treaties as weapons, wielded false promises to justify displacement, and undermined Indigenous sovereignty through legal manipulations.</p>  
                  
                <p>In many ways, Chief Joseph's fight did not end with his death.  His descendants and the Nez Perce people continue reclaiming their rights, stories, and land.  The struggle for Indigenous sovereignty over language, culture, and land remains, echoing the words of their ancestor, who refused to accept the erasure of his people.</p>  
                  
                <p><i>I claim a right to live on my land, and accord you the privilege to return to yours" (Chief Joseph, qtd. in Warren 306)</i></p>  
                  
                <p>Today, Chief Joseph's legacy is recorded in the history books and in the voices of those who continue to fight for Indigenous sovereignty.  He remains a towering figureâ€”a reminder that the fight for justice is never in vain, no matter how long.</p>  
                  
                  
                  
